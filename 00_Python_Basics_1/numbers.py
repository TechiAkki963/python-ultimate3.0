# int and float
print(2 + 4)   # 6
print(2 - 4)   # -2
print(2 * 4)   # 8
print(2 / 4)   # 0.5

# To check the type
print(type(2 + 6))     # <class 'int'>

print(type(2 / 4))     # <class 'float'>

# Why we need float and int
# Because float take a bit more space than the int in the memory
# https://docs.python.org/3/tutorial/floatingpoint.html


print(type(20 + 1.11))  # <class 'float'>

print(2 ** 3)

