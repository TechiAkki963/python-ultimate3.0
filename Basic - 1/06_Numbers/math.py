import math

# Trigonometric functions
result1 = math.sin(math.pi / 2)  # 1
result2 = math.cos(0)  # 1
result3 = math.tan(math.pi / 4)  # 1

# Logarithmic functions
result4 = math.log(math.e)  # 1
result5 = math.log10(100)  # 2
result6 = math.log2(8)  # 3

# Exponential functions
result7 = math.exp(0)  # 1

# Rounding functions
result8 = math.ceil(2.3)  # 3
result9 = math.floor(2.3)  # 2
result10 = math.trunc(2.3)  # 2
result11 = math.round(2.3)  # 2

# Other functions
result12 = math.sqrt(16)  # 4
result13 = math.pow(2, 3)  # 8
result14 = math.factorial(5)  # 120
result15 = math.hypot(3, 4)  # 5
result16 = math.degrees(math.pi / 2)  # 90
result17 = math.radians(180)  # 3.141592653589793

print(result1, result2, result3, result4, result5, result6, result7,
      result8, result9, result10, result11, result12, result13, result14,
      result15, result16, result17)